//# Variable
//? fungsi: menyimpan informasi atau nilai yang akan dikelola dalam sebuah program.

// * Variabel dengan tipe data var
// Variabel dengan tipe data global scope dan dapat diakses oleh seluruh bagian dari kode JavaScript.
var name = "John";

// * Variabel dengan tipe data let
// Variabel dengan tipe data block scope dan hanya dapat diakses di dalam blok yang sama atau scope yang sama.
let age = 30;

// * Variabel dengan tipe data const
// Variabel dengan tipe data konstan atau tidak dapat diubah setelah nilai awal diberikan.
const PI = 3.14;
